export function BlackShadow() {
    return {
        shadowColor: 'black',
        shadowOffset: {
            width: -5,
            height: 5,
        },
        shadowOpacity: 1,
        shadowRadius: 5,
        elevation: 100
    }
}
export function BlackShadowForBlack() {
    return {
        shadowColor: 'black',
        shadowOffset: {
            width: -5,
            height: 5,
        },
        shadowOpacity: 0.8,
        shadowRadius: 5,
        elevation: 100
    }
}